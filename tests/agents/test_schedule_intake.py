CLINIC = "clinic-uuid-1"


def _q(qid: str, order: int, category: str, text: str, required: bool = True, source: str = "clinic"):
    return {
        "id": qid,
        "order": order,
        "question_text": text,
        "category": category,
        "is_required": required,
        "source": source,
    }


@pytest.fixture
def five_baseline_questions() -> List[Dict[str, Any]]:
    return [
        _q("q-med-001", 1, "medicamentos", "Você toma algum medicamento regularmente?"),
        _q("q-alg-002", 2, "alergias", "Tem alguma alergia conhecida?"),
        _q("q-gest-003", 3, "gestacao", "Está grávida ou amamentando?"),
        _q("q-cron-004", 4, "cronicas", "Tem doença crônica como diabetes, pressão alta etc?"),
        _q("q-pele-005", 5, "pele", "Como está sua pele neste momento?"),
    ]


@pytest.fixture
def session_data_evaluation_botox() -> List[Dict[str, Any]]:
    return [{"name": "evaluation", "data": {"service": "botox", "intake_answers": []}}]


def _mock_lm_returning(*payloads: Dict[str, Any]) -> MagicMock:
    """Return a MagicMock LM that yields the given JSON payloads in order.

    Each LM call returns a list with the next JSON string. Used to simulate
    multi-turn flows in a single test.
    """
    lm = MagicMock()
    side_effects = [[json.dumps(p)] for p in payloads]
    lm.side_effect = side_effects
    return lm


# Empty LM mock — when the agent does NOT call the LM (e.g. cold start
# generation only, or empty-questions short-circuit). We still allow it being
# invoked once for natural variant generation.
def _mock_lm_text(text: str) -> MagicMock:
    lm = MagicMock()
    lm.return_value = [
        json.dumps(
            {
                "next_question_text": text,
                "parsed_answers": [],
                "reasoning": "cold start",
            }
        )
    ]
    return lm


