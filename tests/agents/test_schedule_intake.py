        assert out["conversation_stage"] == "schedule_intake"
        assert out["data"]["sub_intent_complete"] is False
        assert out["data"]["next_hint"] is None
        assert out["data"]["next_question_id"] == "q-med-001"
        assert out["data"]["intake_answers"] == []
        # Message generated by LM is in output
        assert out["messages"][0]["type"] == "text"
        assert "medicamento" in out["messages"][0]["content"].lower()


